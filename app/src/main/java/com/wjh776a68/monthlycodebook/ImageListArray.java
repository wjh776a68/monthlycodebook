package com.wjh776a68.monthlycodebook;

public class ImageListArray {
    private String name;
    private int imageId;
    public ImageListArray(String name, int imageId){
        this.name = name;
        this.imageId = imageId;
    }
    public String getName() {
        return name;
    }
    public int getImageId() {
        return imageId;
    }
}
